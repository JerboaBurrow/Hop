    {0.5, -0.25, 0.125},
    {0.25, -0.25, 0.125},
    {0.0, -0.25, 0.125},
}

s = 6.8*hop.maxCollisionPrimitiveSize()

math.randomseed(os.time())

xx = 0.89-s;
x = xx
y = 0.47;

for i = 1, 8 do

    if (x > 1.5) then
        x = xx 
        y = y + s*1.5
    end

    x = x + s*1.5

    o = {

        ["transform"] = {x,y,0.0,s},
        ["colour"] = {200/255,250/255,250/255,1.0},
        ["moveable"] = true,
        ["collisionMesh"] = Square,
        ["name"] = ""

    }

    hop.loadObject(o)

    x = x + s

    o = {

        ["transform"] = {x,y,0.0,s},
        ["colour"] = {200/255,250/255,250/255,1.0},
        ["moveable"] = true,
        ["collisionMesh"] = Corner,
        ["name"] = ""

    }

    hop.loadObject(o)
end